LS = 299792458
LM = LS*60
LH = LM*60
LD = LH*24
LW = LH*7
LY = 365//7 * LW + 365%7 * LD

print(f"Light-second(LS) is {LS} metres.")
print(f"Light-minute(LS) is {LM} metres.")
print(f"Light-hour(LS) is {LH} metres.")
print(f"Light-day(LS) is {LD} metres.")
print(f"Light-week(LS) is {LW} metres.")
print(f"Light-year(LS) is {LY} metres.")
